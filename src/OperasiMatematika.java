public class OperasiMatematika {
  public static void main(String[] args) {

    int a = 10;
    int b = 5;

    System.out.println(a + b);
    System.out.println(a - b);
    System.out.println(a * b);
    System.out.println(a / b);
    System.out.println(a % b);

    a *= 10;
    System.out.println(a);

    b *= 10;
    System.out.println(b);

    int tes = 5;
    int tes2 = 5;

    tes++;
    System.out.println(tes);

    tes2--;
    System.out.println(tes2);

  }
}
